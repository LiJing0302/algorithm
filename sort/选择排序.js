const arr = [8, 2, 5, 9, 4];
/**
 * 选择排序
 * 时间复杂度 O(n^2) 空间复杂度O(1)
 *
 * 选择排序的思想是假定一个最小元素，通过一次遍历，可以选择出确定的最小元素，并与之交换；
 */

const sort = (arr) => {
  for (let i = 0; i < arr.length; i++) {
    let minIndex = i;
    for (let j = i + 1; j < arr.length; j++) {
      if (arr[minIndex] > arr[j]) {
        minIndex = j;
      }
    }
    if (minIndex !== i) {
      const temp = arr[i];
      arr[i] = arr[minIndex];
      arr[minIndex] = temp;
    }
  }
  return arr;
};

